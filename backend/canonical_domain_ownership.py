def _artifact(
    kind: str,
    name: str,
    source_file: str,
    classification: str,
    *,
    symbol: str | None = None,
) -> dict[str, str]:
    return {
        "artifact_id": f"{kind}:{source_file}:{symbol or name}",
        "kind": kind,
        "name": name,
        "source_file": source_file,
        "symbol": symbol or name,
        "classification": classification,
    }


def _tenant(
    *,
    scope: str = "agency",
    tenant_key: str | None = "agency_id",
    ownership_rules: str | None = None,
    portal_visibility: str = "not_portal_visible",
    created_by_field: str | None = "created_by_user_id",
    updated_by_field: str | None = "updated_by_user_id",
    immutable_tenant_fields: tuple[str, ...] | None = None,
) -> dict[str, Any]:
    if immutable_tenant_fields is None:
        immutable_tenant_fields = (tenant_key,) if tenant_key else ()
    return {
        "scope": scope,
        "tenant_key": tenant_key,
        "ownership_rules": ownership_rules
        or (
            "Record agency_id must match the authorized agency membership; "
            "platform access uses existing platform authorization."
            if tenant_key
            else "Platform-global ownership uses existing platform authorization."
        ),
        "portal_visibility": portal_visibility,
        "required_actor_fields": tuple(
            field for field in (created_by_field, updated_by_field) if field
        ),
        "created_by_field": created_by_field,
        "updated_by_field": updated_by_field,
        "immutable_tenant_fields": immutable_tenant_fields,
    }


